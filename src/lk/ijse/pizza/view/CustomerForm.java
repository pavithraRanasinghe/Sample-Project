    private void loadCustomerTable() {
        try {
            List<CustomerDTO> all = CustomerController.getAll();
            DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();
            model.setRowCount(0);
            for (CustomerDTO customerDTO : all) {
                Object[] objects = {customerDTO.getCustomerID(), customerDTO.getName(), customerDTO.getAddress(), customerDTO.getContact(), customerDTO.getEmail()};
                model.addRow(objects);
            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void clearTextFields() {
        txtName.setText("");
        txtAddress.setText("");
        txtContact.setText("");
        txtEMail.setText("");
    }

