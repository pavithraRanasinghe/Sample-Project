    private void btnDeletelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletelActionPerformed
        try {
            if (customerID == 0) {
                JOptionPane.showMessageDialog(this, "No selected customer");
                return;
            }
            boolean deleteCustomer = CustomerController.deleteCustomer(customerID);
            if (deleteCustomer) {
                loadCustomerTable();
                clearTextFields();
                JOptionPane.showMessageDialog(this, "Customer deleted successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Ooops...! Something went wrong");
            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeletelActionPerformed


