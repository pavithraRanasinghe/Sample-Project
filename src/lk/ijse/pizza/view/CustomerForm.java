    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (customerID == 0) {
            JOptionPane.showMessageDialog(this, "No customer selected");
        } else {
            String name = txtName.getText();
            String address = txtAddress.getText();
            String contact = txtContact.getText();
            String email = txtEMail.getText();

            CustomerDTO customerDTO = new CustomerDTO();
            customerDTO.setCustomerID(customerID);
            customerDTO.setName(name);
            customerDTO.setAddress(address);
            customerDTO.setContact(contact);
            customerDTO.setEmail(email);

            try {
                boolean updateCustomer = CustomerController.updateCustomer(customerDTO);
                if (updateCustomer) {
                    customerID = 0;
                    loadCustomerTable();
                    JOptionPane.showMessageDialog(this, "Customer updated successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "Ooops...! Something went wrong");
                }
            } catch (Exception ex) {
                Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtName.getText().equals("") || txtContact.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Input name and contact");
        } else {
            String name = txtName.getText();
            String address = txtAddress.getText();
            String contact = txtContact.getText();
            String email = txtEMail.getText();

            CustomerDTO customerDTO = new CustomerDTO(customerID, name, address, contact, email);

            try {
                boolean addCustomer = CustomerController.addCustomer(customerDTO);
                if (addCustomer) {
                    customerID = 0;
                    loadCustomerTable();
                    JOptionPane.showMessageDialog(this, "Customer added succsefully");
                    clearTextFields();

                } else {
                    JOptionPane.showMessageDialog(this, "Ooops...! something went wrong");
                }
            } catch (Exception ex) {
                Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_btnAddActionPerformed

    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        int selectedRow = tblCustomer.getSelectedRow();
        customerID = (int) tblCustomer.getValueAt(selectedRow, 0);
        String name = (String) tblCustomer.getValueAt(selectedRow, 1);
        String address = (String) tblCustomer.getValueAt(selectedRow, 2);
        String contact = (String) tblCustomer.getValueAt(selectedRow, 3);
        String email = (String) tblCustomer.getValueAt(selectedRow, 4);

        txtName.setText(name);
        txtAddress.setText(address);
        txtContact.setText(contact);
        txtEMail.setText(email);

    }//GEN-LAST:event_tblCustomerMouseClicked
